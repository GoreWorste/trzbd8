"""Генерация отчёта Word (python-docx) по учебному проекту."""

from __future__ import annotations

import os

from docx import Document
from docx.shared import Pt

OUT = os.path.join(os.path.dirname(__file__), "Отчет.docx")


def main() -> None:
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(12)

    doc.add_heading("Рекомендательная система подбора покемонов", 0)
    doc.add_paragraph(
        "Цель: реализовать на Python простую рекомендательную систему по текстовому запросу "
        "с методом взвешенного перебора, возможностью сравнения наборов весов, поиском по имени "
        "и диапазону характеристик, топом слабых покемонов, отзывами и веб-интерфейсом на Flask."
    )

    doc.add_heading("1. Данные", level=1)
    doc.add_paragraph(
        "Используется файл Pokemon.csv, сформированный из открытых CSV проекта veekun/pokedex "
        "(идентификатор, имя, два типа, суммарные и покомпонентные базовые характеристики). "
        "Загрузка выполняется библиотекой pandas."
    )

    doc.add_heading("2. Метод взвешенного перебора", level=1)
    doc.add_paragraph(
        "Для каждого покемона вычисляется сумма баллов. За совпадение запрошенного типа "
        "(русские ключевые слова: огонь, вода, трава, электричество, псих, дракон) начисляется "
        "40–50 баллов (диапазон задаётся конфигурацией; в реализации добавлен стабильный джиттер "
        "от id для демонстрации разброса в пределах интервала)."
    )
    doc.add_paragraph(
        "Ключевые слова «сильный», «быстрый», «живучий», «умный» добавляют до заданного потолка "
        "баллов пропорционально нормализованным по всему набору данных характеристикам "
        "(атака и спец. атака для силы, скорость для быстроты, HP и защиты для живучести, "
        "спец. атака и спец. защита для «умности»)."
    )
    doc.add_paragraph(
        "Процент совместимости: отношение набранных баллов к теоретическому максимуму для данного "
        "запроса (сумма верхних границ по всем сработавшим правилам), умноженное на 100 и ограниченное сверху 100%."
    )

    doc.add_heading("3. Расширяемость и сравнение подходов", level=1)
    doc.add_paragraph(
        "Параметры очков вынесены в dataclass ScoringConfig. Доступны варианты default, strict и generous "
        "(разные верхние границы за тип и ключевые слова), что позволяет сравнивать чувствительность ранжирования."
    )

    doc.add_heading("4. Дополнительный функционал", level=1)
    doc.add_paragraph(
        "Поиск по имени (подстрока без учёта регистра). Топ слабых: сортировка по минимальному полю total. "
        "Фильтр по диапазону числовой характеристики (attack, hp, total и др.). Отзывы сохраняются в reviews.json; "
        "агрегаты (число отзывов, средняя оценка, объём текста) выгружаются в CSV."
    )

    doc.add_heading("5. Интерфейсы", level=1)
    doc.add_paragraph(
        "Консольный цикл: скрипт cli.py. Оконный веб-интерфейс: Flask-приложение app.py, шаблон templates/index.html."
    )

    doc.add_heading("6. Запуск", level=1)
    doc.add_paragraph(
        "python -m venv .venv && .venv/bin/pip install -r requirements.txt\n"
        ".venv/bin/python app.py  # веб\n"
        ".venv/bin/python cli.py  # консоль"
    )

    doc.save(OUT)
    print("Создан файл:", OUT)


if __name__ == "__main__":
    main()
